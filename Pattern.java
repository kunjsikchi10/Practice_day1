public class Pattern

{

public static void main(String args[])   

{   

// the rows to print  

int i, j, row=4;   

//the outer loop for rows  

for(i=0; i<row; i++)   

{   

//the inner loop for columns  

for(j=0; j<=i; j++)   

{   

//to prints stars   

System.out.print("* ");   

}   

// print new line 

System.out.println();   

}   

}

}